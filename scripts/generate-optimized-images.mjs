import fs from 'node:fs/promises';
import path from 'node:path';
import sharp from 'sharp';

const rootDir = process.cwd();
const assetsDir = path.join(rootDir, 'src', 'assets');
const outputDir = path.join(rootDir, 'public', 'optimized');
const manifestPath = path.join(rootDir, 'src', 'assets', 'optimized-images.js');
const widths = [320, 480, 768, 1024, 1360, 1920];
const extensions = new Set(['.jpg', '.jpeg', '.png']);

const toPosix = (value) => value.split(path.sep).join('/');

const slugify = (value) =>
  value
    .toLowerCase()
    .replace(/\.[^.]+$/, '')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');

async function listImages(dir) {
  const entries = await fs.readdir(dir, { withFileTypes: true });
  const files = await Promise.all(
    entries.map(async (entry) => {
      const fullPath = path.join(dir, entry.name);

      if (entry.isDirectory()) {
        return listImages(fullPath);
      }

      return extensions.has(path.extname(entry.name).toLowerCase()) ? [fullPath] : [];
    }),
  );

  return files.flat();
}

await fs.rm(outputDir, { recursive: true, force: true });
await fs.mkdir(outputDir, { recursive: true });

const images = await listImages(assetsDir);
const manifest = {};

for (const imagePath of images) {
  const relativePath = toPosix(path.relative(assetsDir, imagePath));
  const metadata = await sharp(imagePath).metadata();
  const intrinsicWidth = metadata.width;
  const intrinsicHeight = metadata.height;

  if (!intrinsicWidth || !intrinsicHeight) {
    continue;
  }

  const targetWidths = widths.filter((width) => width < intrinsicWidth);
  targetWidths.push(intrinsicWidth);

  const uniqueWidths = [...new Set(targetWidths)].sort((a, b) => a - b);
  const dirSlug = slugify(path.dirname(relativePath) === '.' ? '' : path.dirname(relativePath));
  const fileSlug = slugify(path.basename(relativePath));
  const prefix = dirSlug ? `${dirSlug}-${fileSlug}` : fileSlug;
  const variants = [];

  for (const width of uniqueWidths) {
    const height = Math.round((intrinsicHeight / intrinsicWidth) * width);
    const fileName = `${prefix}-${width}w.webp`;
    const outputPath = path.join(outputDir, fileName);

    await sharp(imagePath)
      .rotate()
      .resize({ width, withoutEnlargement: true })
      .webp({ quality: 78, effort: 6 })
      .toFile(outputPath);

    variants.push({
      width,
      height,
      src: `/optimized/${fileName}`,
    });
  }

  const fallback = variants[variants.length - 1];

  manifest[relativePath] = {
    src: fallback.src,
    width: intrinsicWidth,
    height: intrinsicHeight,
    srcSet: variants.map((variant) => `${variant.src} ${variant.width}w`).join(', '),
    variants,
  };
}

const manifestContent = `// Generated by scripts/generate-optimized-images.mjs\nexport const optimizedImages = ${JSON.stringify(manifest, null, 2)};\n\nexport const getOptimizedImage = (key) => optimizedImages[key];\n`;

await fs.writeFile(manifestPath, manifestContent);

console.log(`Optimized ${Object.keys(manifest).length} images into ${toPosix(path.relative(rootDir, outputDir))}`);
